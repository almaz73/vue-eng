        <p>Intermodal Freight Transport</p>
        <p>1. A freight village is a complex set of facilities where all the activities relating to transport, logistics
            and distribution of goods are carried out on a commercial basis by various operators, who can either be the
            owners or the tenants of the spaces (warehouses, storage areas, offices, car parks etc.). It must be
            equipped with public facilities and, if possible, include public services for the staff and users. Other
            names for a freight village are: logistics park/centre, transport centre or logistics hub.</p>
        <p>2. A freight village enables change from one given transport mode to another (modal shift) through a set of
            technologies that facilitate the transfer. It is served by several transport modes (road, rail, deep sea,
            inland waterway, air) to encourage intermodal transport for the handling of goods. The most common examples
            of modal shifts are: train (rail) to lorry (road); barge (inland waterway) to train or lorry; airplane (air)
            to lorry.</p>
        <p>3. A freight village requires different activities such as warehousing, economic activities, support
            activities, unified management. The warehouse is the infrastructure where the transport operator mostly
            performs his business. This activity may include the division of the goods into smaller quantities for a
            more functional distribution. Logistics hubs need active distribution centres and several industrial
            activities in the neighbourhood that can exploit the modal shift facilities within the village. Support
            activities include support services like lorry rest areas, office space, restaurants, banking, shops and
            hotels. Unified Management requires that the village is often under the management of a single entity.</p>
        <p>4. A freight village is the right solution to satisfy the increasing requirements of a complex business based
            on transport. In order to work well it is imperative that the village is run by a single body, either public
            or private.</p>
